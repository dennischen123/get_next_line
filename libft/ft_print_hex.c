#include "libft.h"

void	ft_print_hex(unsigned int n)
{
	if (n >= 16)
		ft_print_hex(n / 16);
	if (n % 16 < 10)
		ft_putchar((n % 16) + '0');
	else
		ft_putchar((n % 16) - 10 + 'a');
}
